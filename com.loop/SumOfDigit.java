package com.loop;

public class SumOfDigit {

	public static void main(String[] args) {

		int num1 = 56;

		int i = 1;

		int sum = 0;

		while (i <= num1) {

			System.out.print(i + "+");

			sum = sum + i;

			i = i + 1;

		}
		
		
		System.out.println("=  " + sum);

	}

}


